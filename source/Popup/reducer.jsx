
import {ADD_TITLE,EDIT_TITLE,UPDATE_TITLE,COPY_CONTENT,EDIT_CONTENT,DELETE_NOTE,ADD_NOTE} from './constants';

export default notesReducer = (state=[],action)=>{
    switch(action.type){
        case ADD_TITLE:

        case EDIT_TITLE:

        case UPDATE_TITLE:

        case COPY_CONTENT:

        case EDIT_CONTENT:

        case DELETE_NOTE:

        case ADD_NOTE:

        default: return state;
    }
}